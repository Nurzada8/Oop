public class Car {
   private String name;
   private int year ;
   private String color ;

    public String getName() {
        return name;
    }

    public void setName(String make) {
        this.name = make;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
